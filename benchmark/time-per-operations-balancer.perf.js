import { BinaryArrayBalancer } from '../src/BinaryArrayBalancer.js';
const regularArray = [];
const binaryArray = new BinaryArrayBalancer();
const iterations = 100000;
console.log('<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<');
console.log('----------Binary Array Balancer------------');
console.log('>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>');
console.time('binary array balancer total');
console.time('binary array balancer insert at start');
for (let i = 0; i < iterations; i++) binaryArray.unshift(i);
console.timeEnd('binary array balancer insert at start');
console.time('binary array balancer get');
binaryArray.get(Math.floor(binaryArray.size / 2));
console.timeEnd('binary array balancer get');
console.time('binary array balancer remove at start');
for (let i = 0; i < iterations; i++) binaryArray.shift();
console.timeEnd('binary array balancer remove at start');
console.time('binary array balancer insert at end');
for (let i = 0; i < iterations; i++) binaryArray.push(i);
console.timeEnd('binary array balancer insert at end');
console.time('binary array balancer get');
binaryArray.get(Math.floor(binaryArray.size / 2));
console.timeEnd('binary array balancer get');
console.time('binary array balancer remove at end');
for (let i = 0; i < iterations; i++) binaryArray.pop();
console.timeEnd('binary array balancer remove at end');
console.timeEnd('binary array balancer total');

console.log('<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<');
